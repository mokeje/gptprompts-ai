// Helper functions
export function getPagesByCategory(categoryId: string): PageEntry[] {
  return allPages.filter((p) => p.category === categoryId)
}

export function getFeaturedPages(): PageEntry[] {
  return allPages.filter((p) => p.featured === true)
}

export function searchPages(query: string): PageEntry[] {
  const q = query.toLowerCase()
  return allPages.filter(
    (p) =>
      p.label.toLowerCase().includes(q) ||
      (p.description && p.description.toLowerCase().includes(q)) ||
      (p.tags && p.tags.some((t) => t.includes(q)))
  )
}

export function getCategoryById(id: string) {
  return categories.find((c) => c.id === id)
}
